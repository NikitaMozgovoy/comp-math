n=8
e=0.000002
x=0.5
a=[0.9999998, 1.0000000, 0.5000063, 0.1666674, 0.0416350, 0.0083298, 0.0014393, 0.0002040]
c= a[0]
p=1
for k in range(0, n):
    p*=x
    u=p * a[k]
    print(u)
    c+=u
    if abs(u)<=e:
        print("c = %f" %c)
        print("K = %f" %k)
        break
    print("Требуемая точность не достигнута")